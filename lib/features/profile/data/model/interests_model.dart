class Interest {
  final String id;
  final String name;
  final String color;
  bool isSelected;

  Interest({
    required this.id,
    required this.name,
    required this.color,
    this.isSelected = false,
  });

  Interest copyWith({
    String? id,
    String? name,
    String? color,
    bool? isSelected,
  }) {
    return Interest(
      id: id ?? this.id,
      name: name ?? this.name,
      color: color ?? this.color,
      isSelected: isSelected ?? this.isSelected,
    );
  }
}
